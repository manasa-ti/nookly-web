class RecommendedProfile {
  final String id;
  final String name;
  final int age;
  final String gender;
  final int distance;
  final String bio;
  final List<String> interests;
  final String profilePicture;

  const RecommendedProfile({
    required this.id,
    required this.name,
    required this.age,
    required this.gender,
    required this.distance,
    required this.bio,
    required this.interests,
    required this.profilePicture,
  });
} 